	/**
	 * message to xml
	 * 
	 * @param newsMessage
	 * @param clazz
	 * @return
	 * @description
	 * @version currentVersion
	 * @author Yuanbo Zhu
	 * @createtime 27 Jul, 2015 4:45:42 pm
	 */
	public static String messageToXml(Object newsMessage) {

		if (newsMessage instanceof TextMessage) {
			TextMessage msg = (TextMessage) newsMessage;
			return textMessageToXml(msg);
		} else if (newsMessage instanceof NewsMessage) {
			NewsMessage nmsg = (NewsMessage) newsMessage;
			return newsMessageToXml(nmsg);
		} else if (newsMessage instanceof MusicMessage) {
			MusicMessage nmsg = (MusicMessage) newsMessage;
			return musicMessageToXml(nmsg);
		} else {
			return "unknow format";
		}

	}

	/**
	 * 扩展xstream，使其支持CDATA块
	 * 
	 * @date 2013-05-19
	 */
	private static XStream xstream = new XStream(new XppDriver() {
		public HierarchicalStreamWriter createWriter(Writer out) {
			return new PrettyPrintWriter(out) {
				// 对所有xml节点的转换都增加CDATA标记
				boolean cdata = true;

				@SuppressWarnings("unchecked")
				public void startNode(String name, Class clazz) {
					super.startNode(name, clazz);
				}

				protected void writeText(QuickWriter writer, String text) {
					if (cdata) {
						writer.write("<![CDATA[");
						writer.write(text);
						writer.write("]]>");
					} else {
						writer.write(text);
					}
				}
			};
		}
	});
